// Convert emoji to readable name using shortcodes
function emojiToName(emoji) {
    try {
        const emojiMap = require('emoji-unicode-map');
        const shortcode = emojiMap.get(emoji);

        if (shortcode) {
            // Convert shortcode to readable name
            // :gold_medal: → "gold medal"
            // :american_football: → "american football"
            return shortcode
                .replace(/_/g, ' ')
                .replace(/-/g, ' ')
                .toLowerCase();
        }
    } catch (error) {
        // If package not available, return null to use fallback
        console.warn('emoji-unicode-map not available:', error.message);
    }

    return null;
}

